## " " do rong cua chuoi s
s = "abcd"
z = s.center(10, "-")
print(z)

## canh le trai
s = "abcd"
z = s.ljust(10, "*")
print(z)

## canh le phai
s = "abcd"
z = s.rjust(10, "-")
print(z)

## viet hoa chu cai dau cau
s = "abcd"
z = s.capitalize()
print(z)

## viet thuong ca chuoi
s = "ABCD"
z = s.lower()
print(z)

## viet hoa ca chuoi
s = "abcd"
z = s.upper()
print(z)

## viet hoa chu cai dau moi tu
s = "lap trinh python"
z = s.title()
